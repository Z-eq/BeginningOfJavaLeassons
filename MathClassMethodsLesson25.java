package zeq.from.beginning;

// 7 most common Math methods

public class MathClassMethodsLesson25 {
    public static void main(String[] args) {
        System.out.println(Math.abs (-26.7)); // Math.abs = absolute value regardless positive or negative values
        System.out.println(Math.ceil (7.2));  // Math.ceil = always rounds up
        System.out.println(Math.floor (26.7)); //Math.floor = rounds it down
        System.out.println(Math.max(26.7, 45.2));  // Math.max = gives the max number of 2 entered numbers
        System.out.println(Math.min(26.7, 45.2));  // Math.min = gives the minimal number of 2 entered numbers
        System.out.println(Math.pow(5,3));  // Math.pow = 5*5*5
        System.out.println(Math.sqrt(30));  // Math.sqrt  sqrt root of a number
    }
}

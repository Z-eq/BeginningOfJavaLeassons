package zeq.from.beginning;


    // any time you anna do a thing at least once u do a do loop!
  // and any time u want to test it first and only test if true u do while loop!


public class doWhileLoopLesson24 {
    public static void main(String[] args) {

        int counter = 0;

        do{
            System.out.println(counter);
            counter++;
        }while (counter <=10);
    }
}
